cars = ["Honda", "Tesla", "Volvo"]
print(f"I would like to own a {cars[0]} vehicle.")
print(f"I would like to own a {cars[1]} vehicle.")
# By asking for the item at index -1, Python always returns the last item in the list.
print(f"I would like to own a {cars[-1]} vehicle.")
# The index -2 returns the second item from the end of the list.
print(f"I would like to own a {cars[-2]} vehicle.")

